int		ft_count_if(char **tab, int (*f)(char*))
{
	int i;
	int hits;

	hits = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i]) == 1)
			hits++;
		i++;
	}
	return (hits);
}
