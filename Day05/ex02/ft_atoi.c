int	ft_atoi(const char *str)
{
	int i;
	int result;
	int sign;

	sign = 1;
	result = 0;
	i = 0;
	if (str[0] == '-')
	{
		sign = -1;
		i++;
	}
	if (str[0] == '+')
	{
		i++;
	}
	while (str[i] - '0' <= 9)
	{
		if (str[i] - '0' >= 0 && str[i] - '0' <= 9)
			result = result * 10 + (str[i] - '0');
		i++;
	}
	return (sign * result);
}
